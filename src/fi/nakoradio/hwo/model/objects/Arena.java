package fi.nakoradio.hwo.model.objects;

public class Arena {
	
	private float width;
	private float height;
	
	public Arena(){
		
	}

	public float getWidth() {
		return width;
	}

	public void setWidth(float width) {
		this.width = width;
	}

	public float getHeight() {
		return height;
	}

	public void setHeight(float height) {
		this.height = height;
	}
	
	

}
