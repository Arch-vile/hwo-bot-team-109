package fi.nakoradio.hwo.model.objects;

public class StateInTime {

	private long time;
	private float leftPlayerY;
	private float rightPlayerY;
	private float ballX;
	private float ballY;
	private float confMaxWidth;
	private float confMaxHeight;
	private float confPaddleHeight;
	private float confPaddleWidth;
	private float confBallRadius;
	private long confTickInterval;
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public float getLeftPlayerY() {
		return leftPlayerY;
	}
	public void setLeftPlayerY(float leftPlayerY) {
		this.leftPlayerY = leftPlayerY;
	}
	public float getRightPlayerY() {
		return rightPlayerY;
	}
	public void setRightPlayerY(float rightPlayerY) {
		this.rightPlayerY = rightPlayerY;
	}
	public float getBallX() {
		return ballX;
	}
	public void setBallX(float ballX) {
		this.ballX = ballX;
	}
	public float getBallY() {
		return ballY;
	}
	public void setBallY(float ballY) {
		this.ballY = ballY;
	}
	public float getConfMaxWidth() {
		return confMaxWidth;
	}
	public void setConfMaxWidth(float confMaxWidth) {
		this.confMaxWidth = confMaxWidth;
	}
	public float getConfMaxHeight() {
		return confMaxHeight;
	}
	public void setConfMaxHeight(float confMaxHeight) {
		this.confMaxHeight = confMaxHeight;
	}
	public float getConfPaddleHeight() {
		return confPaddleHeight;
	}
	public void setConfPaddleHeight(float confPaddleHeight) {
		this.confPaddleHeight = confPaddleHeight;
	}
	public float getConfPaddleWidth() {
		return confPaddleWidth;
	}
	public void setConfPaddleWidth(float confPaddleWidth) {
		this.confPaddleWidth = confPaddleWidth;
	}
	public float getConfBallRadius() {
		return confBallRadius;
	}
	public void setConfBallRadius(float confBallRadius) {
		this.confBallRadius = confBallRadius;
	}
	public long getConfTickInterval() {
		return confTickInterval;
	}
	public void setConfTickInterval(long confTickInterval) {
		this.confTickInterval = confTickInterval;
	}
	
	
	
}
